#include "libft.h"

unsigned short			ft_numlen(int n)
{
	size_t				out;

	out = 0;
	if (n == -2147483648)
		return (ft_strlen("-2147483648"));
	if (n < 0 && (n = -n))
		out += 1;
	while (n)
	{
		out += 1;
		n /= 10;
	}
	return (out);
}
